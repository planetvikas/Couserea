## This function returns inverse of the matrix.  
## The function first finds if inverse exists in cache otherwise it calculates
## inverse using solve command.

cacheSolve <- function(x, ...) {
  m <- x$getinv()
  if(!is.null(m)) {
    message("getting cached data")
    return(m)
  }
  data <- x$get()
  m <- solve(data, ...)
  x$setinv(m)
  m
}